void printVec3(glm::vec3 v)
{
	cout << "( " << v.x << " " << v.y << " " << v.z << " )" << endl;
}

void printMat4(glm::mat4 m)
{
	cout << "| " << m[0][0] << " " << m[0][1] << " " << m[0][2] << " " << m[0][3] << "| " << endl
		<< "| " << m[1][0] << " " << m[1][1] << " " << m[1][2] << " " << m[1][3] << "| " << endl
		<< "| " << m[2][0] << " " << m[2][1] << " " << m[2][2] << " " << m[2][3] << "| " << endl
		<< "| " << m[3][0] << " " << m[3][1] << " " << m[3][2] << " " << m[3][3] << "| " << endl;
}

